export const normalize = (s) => (s || '').trim().toLowerCase().replace(/\s+/g, '');

export async function gradeSubjective({ question, correctAnswer, userAnswer, rowHeader, colHeader, callLLMWithFailover }) {
  if (!correctAnswer || !userAnswer) {
    return { isCorrect: false, score: 0, reason: '답안이 비어 있습니다.' };
  }

  if (normalize(userAnswer) === normalize(correctAnswer)) {
    return { isCorrect: true, score: 10, reason: '텍스트가 모범 답안과 정확히 일치합니다.' };
  }

  const userPrompt = `
- 문제/맥락: ${question || '주관식 빈칸 채우기'}
${rowHeader ? `- 표 행 제목 (Row Header): ${rowHeader}` : ''}
${colHeader ? `- 표 열 제목 (Column Header): ${colHeader}` : ''}
- 모범 답안: ${correctAnswer}
- 사용자의 답안: ${userAnswer}
`;

  const responseText = await callLLMWithFailover(systemInstruction, userPrompt, null, 'grading');
  let text = responseText.trim();
  if (text.startsWith('```')) {
    text = text.replace(/^```json/, '').replace(/^```/, '').replace(/```$/, '').trim();
  }
  
  try {
    const result = JSON.parse(text);
    return {
      isCorrect: !!result.isCorrect,
      score: typeof result.score === 'number' ? result.score : (result.isCorrect ? 10 : 0),
      reason: result.reason || 'AI 채점 완료'
    };
  } catch (parseErr) {
    console.error('Failed to parse AI grading JSON. Raw text:', text, parseErr);
    try {
      const match = text.match(/\{[\s\S]*\}/);
      if (match) {
        const result = JSON.parse(match[0]);
        return {
          isCorrect: !!result.isCorrect,
          score: typeof result.score === 'number' ? result.score : (result.isCorrect ? 10 : 0),
          reason: result.reason || 'AI 채점 완료 (JSON 추출)'
        };
      }
    } catch (regexParseErr) {
      console.error('Failed to parse extracted JSON:', regexParseErr);
    }
    throw parseErr;
  }
}
